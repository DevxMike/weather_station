void comm::main(){
    static uint8_t comm_state = 0;
    static unsigned long timer = 0;
    msg_t message;

    mqttClient.loop();

    switch(comm_state){
        case 0:
            if(mqttClient.connected()){
                comm_state = 2;
            }
            else{
                connect();
                timer = millis();
                comm_state = 1;
            }
        break;

        case 1:
            if(millis() - timer >= 500){
                if(mqttClient.connected()){
                    comm_state = 2;
                }
                else{
                    timer = millis();
                    comm_state = 0;
                }
            }
        break;

        case 2:
            if(mqttClient.connected()){
                    while(!messages.is_empty()){
                        messages.pop(message);
                        mqttClient.publish(
                            message.topic.c_str(), 
                            message.payload.c_str()
                        );

                        Serial.println(message.topic.c_str());
                        Serial.println(message.payload.c_str());

                    }

                }
                else{
                    comm_state = 0;
                }
        break;
    }
}

void message_stack::push(String topic, String payload){
    if(!is_full()){
        messages[count].payload = payload;
        messages[count].topic = topic;
        ++count;
    }
    else{

    }
}

void message_stack::pop(msg_t& msg){
    if(!is_empty()){
        msg = messages[count - 1];
        --count;
    }
}